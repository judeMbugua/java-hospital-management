    private void computeBill_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_computeBill_btnActionPerformed

        //validate input
        String id = patientId.getText();
        if(id.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter an ID to search for patient");
            return;
        }
        
        //check if a patients with the entered id exists
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection con = DriverManager.getConnection(url,username,password);
            
            Statement cursor = con.createStatement();
            
            //to be used by the second resultSet 
            Statement cursor1 = con.createStatement();
            
        
            ResultSet resultSet = cursor.executeQuery( "SELECT patient_id FROM patient WHERE patient_id IS NOT NULL");
            
        
            ResultSet resultSet2 = cursor1.executeQuery( "SELECT national_id FROM patient WHERE national_id IS NOT NULL");
            
            
            
            //this array list stores the id's of the existing patients in the database
            ArrayList<String> existing = new ArrayList<>();            
            
           
            
            while(resultSet.next()){
                ////getting the values of the patient_id fields and adding them to the list
                existing.add(resultSet.getString("patient_id"));
            }
            

            while(resultSet2.next()){
                //getting the values of the national_id fields and adding them to the list
                existing.add(resultSet2.getString("national_id"));
            }            

            //a boolean that checks whether a patient with the entered id exists
            boolean exists = false;
            
            for(String _id : existing){
                if(_id.equals(id)){
                    //set exists to true
                    exists = true;
                }
            }
            
            //alert if the patient does not exist and ask whether to register them first
            if(!exists){
                //the confirm dialog returns an integer 0,1,2  0 for yes,1 for no and 2 for cancel
                int register = JOptionPane.showConfirmDialog(this, "No patient with such an ID exists, do you want to register them?");
                
                if(register == 0){
                    switchPanel(computeBill_panel,registerPatient_panel);
                }
                else if(register == 1 || register == 2 ){
                    return;
                }
            }
            
            //the patient does exist
            else if(exists){
                //get the name of the patient
                ResultSet rslt = cursor.executeQuery("SELECT name,paid FROM patient WHERE patient_id = '"+id+"' OR national_id = '"+id+"' ");
                
                //moving the cursor to the first row in the returned resultSet
                rslt.next();
                String name = rslt.getString("name");
                String paid = rslt.getString("paid");
                
               //get the serviceOffered
               String service = String.valueOf(billServiceName.getSelectedItem());
               
               //get the cost of the service from the table
               ResultSet rslt2 = cursor1.executeQuery("SELECT cost FROM service WHERE name = '"+service+"' ");
                
                //moving the cursor to the first row in the returned resultSet
                rslt2.next();
                int cost = rslt2.getInt("cost");
                
                //check if the paid statusin the table is yes to avoid double paying
                if(paid.equals("Yes")){
                    JOptionPane.showMessageDialog(this, "This patient with ID number " + id + " had already paid!", "Error,double payment", HEIGHT);
                    searchId.setText("");
                    return;
                }

                int confirmPay = JOptionPane.showConfirmDialog(this, String.format("The total cost for %s with ID number %s is %s.Confirm payment and print receipt?",name,id,cost));
                if(confirmPay == 0){
                    //change the value of the paid field from No to yes in the patients table
                    try {
                        cursor.execute("UPDATE patient SET paid = 'Yes' WHERE patient_id = '"+id+"' OR national_id = '"+id+"' ");
                        JOptionPane.showMessageDialog(this, "Payment successful!");
                        patientId.setText("");
                        
                    } catch (Exception e) {
                        
                        System.out.println(e);
                    }
                }
               patientId.setText("");
            }
            
        }
        catch(Exception e){
                System.out.println(e);
                }
    }//GEN-LAST:event_computeBill_btnActionPerformed

