     //this method checks whether a panel is visible and sets it to visible if not
      //this method also clears the inputs fields of the hidePanel
    public void switchPanel(javax.swing.JPanel hidePanel,javax.swing.JPanel showPanel){
        
        //this list holds the inputs in the menu passed as a parameter
       ArrayList<javax.swing.JTextField> inputs = new ArrayList<>();
       
       //get the components in the panel
       var components = hidePanel.getComponents();
       
       //loop thru the componets and check if they are buttons if so add them in the list
       for(var component : components){
           if(component instanceof JTextField){
               inputs.add((JTextField) component); //casting the component to JTextField and adding it
           }
       }
       
       //clear the inputs in the panel
       for(JTextField input: inputs){
           input.setText("");
       }
       
        hidePanel.setVisible(false);
        showPanel.setVisible(true);
        
    }
