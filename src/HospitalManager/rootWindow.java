    public void changeBG(javax.swing.JPanel currentPanel,String btnText){
        //this list holds the buttons in the menu passed as a parameter
       ArrayList<javax.swing.JButton> buttons = new ArrayList<>();
       
       //get the components in the panel
       var components = currentPanel.getComponents();
       
       //loop thru the componets and check if they are buttons if so add them in the list
       for(var component : components){
           if(component instanceof JButton){
               buttons.add((JButton) component); //castig the component to JButton and adding it
           }
       }
       
       //change the backgrounds to default color except for the "currentButton"
       for(JButton btn: buttons){
           if(!btn.getText().equals(btnText)){
               btn.setBackground(original);
           }
           else if (btn.getText().equals(btnText)){
               btn.setBackground(Color.green);
           }
       }
       
       
        
    }
    
