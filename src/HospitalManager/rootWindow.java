    private void chiefLogin_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chiefLogin_BtnActionPerformed
        
        //input validation
         //checking whether all the spaces are filled
        String fullName = String.valueOf(chiefLoginName.getText());
        String pass = String.valueOf(chiefLoginPass.getPassword());
        
        if(fullName.equals("") || pass.equals("")){
            JOptionPane.showMessageDialog(this, "Please fill in all the spaces!");
            return;
        }
        

        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection con = DriverManager.getConnection(url,username,password);
            
            Statement cursor = con.createStatement();
            
            ResultSet resultSet = cursor.executeQuery("SELECT name FROM chiefadmin");
            
            //this array list stores names of cheif admins currently in the database
            ArrayList<String> names = new ArrayList<>();
            
            while(resultSet.next()){
                //adding the names from the table to the list
                names.add(resultSet.getString("name"));
            }
            
            //check whether the inputted name exists in the database
            boolean exists = false;
            
            
            //loop thru the list checking for the name
            for(String name : names){
                if(name.equals(fullName.toLowerCase())){
                    exists = true;
                }
            }
            if(!exists){
                JOptionPane.showMessageDialog(this, "No such chief admin exists!");
                chiefLoginName.setText("");
                chiefLoginPass.setText("");
                return;
                
            }else if(exists){
                //if chief exists,fetch and compare passwords
                ResultSet rslts = cursor.executeQuery("SELECT * FROM chiefadmin WHERE name = '"+fullName+"'");
                if(rslts.next()){
                    String getPass = rslts.getString("password");
                    
                    //compare the passwords
                    if(getPass.equals(pass.toLowerCase())){
                        //get the name of the logged in chief
                        loggedInChief = fullName;
                        
                        //displaying the name of the currently logged in chief admin
                        loggedChiefLabel.setText(fullName.toLowerCase());
                        switchPanel(loginChief_panel,chiefHome_panel);
                        switchPanel(menuPanel,chiefMenuPanel);
                    }
                    else if(!getPass.equals(pass.toLowerCase())){
                        JOptionPane.showMessageDialog(this,"You have entered the wrong password!");
                        chiefLoginPass.setText("");
                    }
                }
                 
            }
            
            con.close();
                    
      }catch (Exception e) {
            System.out.println(e);
            
        }
    }//GEN-LAST:event_chiefLogin_BtnActionPerformed

